package TiposDeImpressao;

public class TiposDeImpressao {
    public static void main(String[] args){

        int num=2007;
        String nome="Samuel";

        System.out.print("\nSamuel\n"); // Imprime alguma
        System.out.println("Samukinha"); // Imprime um texto em linha
        System.out.printf("Nome:%s - Data de nascimento:%d", "Samuel", 2007); // Imprime alguma coisa formatada
        System.out.printf("Valor da variavel num:%d%nNome:%s",num,nome);
    }
}
